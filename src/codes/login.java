    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(user.getText().isEmpty() || pass.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Username or Password is Empty..");
        }
        else
        {
            try
            {
                String username = user.getText();
                String password = pass.getText();
                
                Class.forName("com.mysql.jdbc.Driver");
                conn = DriverManager.getConnection("jdbc:mysql://localhost/studentmanagementsystem","root","");
                st = conn.prepareStatement("select *  from users where username = ? and password = ? ");
                st.setString(1,username);
                st.setString(2, password);
                rs = st.executeQuery();
                if(rs.next())
                {
                    dashboard m = new dashboard();
                    this.hide();
                    m.setVisible(true);
                }
                else
                {
                    JOptionPane.showMessageDialog(this, "Incorrect Username or Password");
                    user.setText("");
                    pass.setText("");
                    user.requestFocus();
                }
            }
            catch(Exception ex)
            {
                System.out.println("Error: "+ex);
            }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

